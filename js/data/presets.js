// 随机斑块
function generateRandomBlob(size = 10) {
    const pattern = [];
    const center = size / 2;
    for (let y = 0; y < size; y++) {
        const row = [];
        for (let x = 0; x < size; x++) {
            const dist = Math.sqrt((x - center) ** 2 + (y - center) ** 2);
            const maxDist = center;
            if (dist < maxDist) {
                const base = 1 - dist / maxDist;
                row.push(base * (0.5 + Math.random() * 0.5));
            } else {
                row.push(0);
            }
        }
        pattern.push(row);
    }
    return pattern;
}

